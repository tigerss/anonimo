package com.anonimo.api.model;

public class MessageVotes {

	private Number upVotes;
	private Number downVotes;
	
	public MessageVotes() {
		
	}

	public Number getUpVotes() {
		return upVotes;
	}

	public void setUpVotes(Number upVotes) {
		this.upVotes = upVotes;
	}

	public Number getDownVotes() {
		return downVotes;
	}

	public void setDownVotes(Number downVotes) {
		this.downVotes = downVotes;
	}
	
	
}
